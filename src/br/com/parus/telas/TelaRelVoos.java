    //pesquisar todos avi천es
    private void pesquisar_voo() {
        String sql = "select * from pessoa where aviao=?";

        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtRelId.getText());
            rs = pst.executeQuery();
            tblVoos.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

